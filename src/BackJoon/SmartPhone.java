package BackJoon;

public class SmartPhone extends CellPhone {
  String isMonthlyFixedRate;
  String monthlyFixedFee;

  public String getIsMonthlyFixedRate() {
    return isMonthlyFixedRate;
  }
  public void setIsMonthlyFixedRate(String isMonthlyFixedRate) {
    this.isMonthlyFixedRate = isMonthlyFixedRate;
  }
  public String getMonthlyFixedFee() {
    return monthlyFixedFee;
  }
  public void setMonthlyFixedFee(String monthlyFixedFee) {
    this.monthlyFixedFee = monthlyFixedFee;
  }

}
